package com.study.ch04; // 연산자

public class Operation01 {

    public static void main(String[] args) {
        /*
         <<연산자>>

         산술 연산자, 비교연산자, 논리연산자, 조건연산자, 복학대입연산자

         */

        System.out.println("산술연산자");
        System.out.println("1 + 1 = " + ( 1 + 1 ));
        System.out.println("1 - 1 - " + ( 1 - 1 ));
        System.out.println("1 * 1 - " + ( 1 * 1 ));
        System.out.println("1 / 1 - " + ( 1 / 1 )); // 나누기 - 몫
        System.out.println("10 % 2 - " + ( 10 % 2 )); // 나누기 - 나머지값

        // 게시글이 101개가 있으면 10개씩 보여주고 싶을 때 11페이지까지 있어야한다. 나누어 떨어지면 나온게 페이지 번호
        // 나누어 떨어지지 않으면 +1 페이지
        // "즉, 나누어떨어지면 ~을 한다", "나누어 떨어지지 않으면 ~을 한다"라고 할 때 많이 쓴다

    }
}
